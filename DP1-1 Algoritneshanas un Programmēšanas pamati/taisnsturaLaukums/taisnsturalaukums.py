# Pieprasīt abu malu garumus
malaGar = float(input("Ievadiet taisnstūra garākās malas garumu: "))
malaIsa = float(input("Ievadiet taisnstūra īsākās malas garumu: "))

# Sareizināt malu garumus un iegūt laukumus
laukums = malaGar * malaIsa

# Izrakstīt rezūltātu
print(f"Taisnstūra laukums ir: ", laukums)