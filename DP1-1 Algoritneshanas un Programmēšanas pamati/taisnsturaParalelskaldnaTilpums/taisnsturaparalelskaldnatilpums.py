# Pieprasīt nepieciešamos parametrus
garums = float(input("Ievadiet garumu: "))
platums = float(input("Ievadiet platumu: "))
augstums = float(input("Ievadiet augstumu: "))

# Apreiķināt tilpumu
tilpums = garums * platums * augstums

# Izdot rezultātu
print(f"Taisnstura paralelskaldņa tilpums ir: ", tilpums)